package com.gloriabraz.variables;

public class Variaveis {
    public static void main(String[] args) {
        // PRIMITIVE TYPES
        // are data types that are not objects

        // Integer types
        byte b = 2;
        short s = 24;
        int i = 282;
        long lo = 22L; // using L, it is necessary to indicate that it is a long, otherwise, the compiler will consider it an int

        // Decimal types
        float flo = 1.70F; // using F, it is necessary to indicate that it is a float, otherwise, the compiler will consider it a double
        double doub = 10.50;

        // Character types
        char letra = 'L';

        // Boolean type
        boolean verdadeiro = true;

        // NON-PRIMITIVE TYPES
        String texto = "Gl√≥ria";
    }
}
