// Exemplos de valores falsy
console.log("Falsy Values:");
console.log(Boolean(false));      // false é falsy
console.log(Boolean(0));          // 0 é falsy
console.log(Boolean(""));         // string vazia é falsy

// Exemplos de valores truthy
console.log("Truthy Values:");
console.log(Boolean(true));       // true é truthy
console.log(Boolean(28));         // qualquer número diferente de 0 é truthy
console.log(Boolean("Oie :]"));    // qualquer string não vazia é truthy