const primeiroNumero = Number(prompt("Insira um número: "));
const segundoNumero = Number(prompt("Insira outro número: "));

const soma = primeiroNumero + segundoNumero;
const subtracao = primeiroNumero - segundoNumero;
const multiplicacao = primeiroNumero * segundoNumero;
const divisao = primeiroNumero / segundoNumero;

alert("Soma: " + soma +
    "\nSubtração: " + subtracao + 
    "\nMultiplicação: " + multiplicacao +
    "\nDivisão: " + divisao)
