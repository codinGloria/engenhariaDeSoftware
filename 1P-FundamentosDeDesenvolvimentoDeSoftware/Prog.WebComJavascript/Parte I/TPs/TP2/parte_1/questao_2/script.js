const numeroA = Math.floor(Number(prompt("Insira um número inteiro:")));
const numeroB = Math.floor(Number(prompt("Insira outro número inteiro:")));

const resultadoDivisao = numeroA / numeroB;
const restoDivisao = numeroA % numeroB;

alert("O resultado da divisão é: " + resultadoDivisao + "\nO resto da divisão é: " + restoDivisao);